#!/bin/bash -x
read -p "Enter the number" x

if [ $x -eq 0 ]
then
     echo "$x is zero"
elif [ $x -eq 1 ]
then
     echo "$x is one"
elif [ $x -eq 2 ]
then
     echo "$x is two"
elif [ $x -eq 3 ]
then
     echo "$x is three"
elif [ $x -eq 4 ]
then
     echo "$x is four"
elif [ $x -eq 5 ]
then
     echo "$x is five"
elif [ $x -eq 6 ]
then
     echo "$x is six"
elif [ $x -eq 7 ]
then
     echo "$x is seven"
elif [ $x -eq 8 ]
then
     echo "$x is eight"
elif [ $x -eq 9 ]
then
     echo "$x is nine"
fi
